class Translator:
    """Clase para manejar traducciones en la aplicación"""
    
    def __init__(self, language="es"):
        """
        Inicializa el traductor con el idioma especificado
        
        Args:
            language (str): Código de idioma ('es', 'en', 'ko', 'pt', 'it', 'de', 'ja')
        """
        self.set_language(language)
    
    def set_language(self, language):
        """
        Establece el idioma actual para traducciones
        
        Args:
            language (str): Código de idioma ('es', 'en', 'ko', 'pt', 'it', 'de', 'ja')
        """
        if language not in TRANSLATIONS:
            language = "es"  # Default to Spanish if language not found
        self.language = language
    
    def get_text(self, key):
        """
        Obtiene la traducción para una clave específica
        
        Args:
            key (str): Clave de traducción
            
        Returns:
            str: Texto traducido o la clave original si no se encuentra
        """
        if key in TRANSLATIONS[self.language]:
            return TRANSLATIONS[self.language][key]
        
        # Si la clave no existe en el idioma actual, intentar con español
        if self.language != "es" and key in TRANSLATIONS["es"]:
            return TRANSLATIONS["es"][key]
            
        # Si no se encuentra, devolver la clave
        return key
    
    def get_available_languages(self):
        """
        Obtiene los idiomas disponibles
        
        Returns:
            dict: Diccionario con códigos de idioma y nombres
        """
        return {
            "es": "Español",
            "en": "English",
            "ko": "한국어",
            "pt": "Português",
            "it": "Italiano",
            "de": "Deutsch",
            "ja": "日本語"
        }